def build_prompt(name: str, category: str, features: str, tone: str) -> str:
    return f"""Ты — копирайтер для интернет-магазина. Напиши описание товара на русском языке.

Товар: {name}
Категория: {category}
Характеристики: {features}
Тон текста: {tone}

Требования:
- Длина: 200–300 слов
- Начни с цепляющего предложения о пользе для покупателя
- Упомяни ключевые характеристики естественно, без перечисления списком
- Заверши призывом к действию
- Никаких заголовков, только сплошной текст

Напиши только текст описания, без предисловий и комментариев."""